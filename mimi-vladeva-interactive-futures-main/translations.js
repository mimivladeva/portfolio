// Keys that contain HTML (need innerHTML instead of textContent)
const HTML_KEYS = new Set(["about_p1", "about_p2", "about_p3"]);

function applyLang(lang) {
    const t = translations[lang];
    if (!t) return;

    localStorage.setItem("lang", lang);

    // Highlight active button
    document.querySelectorAll(".lang-btn").forEach(btn => {
        btn.classList.toggle("active", btn.textContent.trim() === lang);
    });

    // Translate all [data-i18n] elements
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (t[key] === undefined) return;
        if (HTML_KEYS.has(key)) {
            el.innerHTML = t[key];
        } else {
            el.textContent = t[key];
        }
    });

    // Translate all [data-i18n-placeholder] elements
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (t[key] !== undefined) el.placeholder = t[key];
    });
}

document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll(".lang-btn").forEach(btn => {
        btn.addEventListener("click", () => applyLang(btn.textContent.trim()));
    });

    applyLang(localStorage.getItem("lang") || "ES");
});